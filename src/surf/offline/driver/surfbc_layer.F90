SUBROUTINE SURFBC_LAYER_PARALLEL(PSURF, PSURFL, PCLIM, PAUX, PINTER, LLKEYS, ON_GPU)

TYPE(SURF_AND_MORE_TYPE)        , INTENT(INOUT) :: PSURF
TYPE(SURF_AND_MORE_LOCAL_TYPE)  , INTENT(INOUT) :: PSURFL
TYPE(CLIMATE_TYPE)              , INTENT(INOUT) :: PCLIM
TYPE(AUX_TYPE)                  , INTENT(INOUT) :: PAUX
TYPE(INTERNAL_TYPE)             , INTENT(INOUT) :: PINTER
TYPE(KEYS_LOCAL_TYPE)           , INTENT(INOUT) :: LLKEYS
LOGICAL                         , INTENT(IN)    :: ON_GPU

IF (ON_GPU) THEN
  CALL SURFBC_LAYER_LOKI(PSURF, PSURFL, PCLIM, PAUX, PINTER, LLKEYS)
ELSE
  CALL SURFBC_LAYER_OPENMP(PSURF, PSURFL, PCLIM, PAUX, PINTER, LLKEYS)
ENDIF

END SUBROUTINE SURFBC_LAYER_PARALLEL

